#include <unistd.h>

char	*ft_strupcase(char *str)
{
	if (*str == '\0')
	{
		return (str);
	}
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str - 32;
		}
		str++;
	}
	return (str);
}
