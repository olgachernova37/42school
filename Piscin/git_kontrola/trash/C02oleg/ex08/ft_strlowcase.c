#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	if (*str == '\0')
	{
		return (str);
	}
	while (*str)
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str + 32;
		}
		str++;
	}
	return (str);
}
