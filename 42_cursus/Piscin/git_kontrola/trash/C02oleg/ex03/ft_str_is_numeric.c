#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
	{
		return (1);
	}
	while (*str)
	{
		if (str[i] > '9')
		{
			return (0);
		}
		i++;
		str++;
	}
	return (1);
}
