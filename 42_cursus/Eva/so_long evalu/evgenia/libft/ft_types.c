#include "libft.h"

int	ft_isflag(int c)
{
	if (c == '-' || c == '.' || c == '#' || c == ' '
		|| c == '+' || c == '*' || ft_isdigit(c))
		return (1);
	else
		return (0);
}

int	ft_istype(int c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd'
		|| c == 'i' || c == 'u' || c == 'x' || c == 'x'
		|| c == 'X' || c == '%')
		return (1);
	else
		return (0);
}
